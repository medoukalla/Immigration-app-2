<!-- jQuery to Control Modal Display -->
<script src="https://code.jquery.com/jquery-3.6.0.min.js"></script>
<script>
  $(document).ready(function () {
    // Show modal when button is clicked
    $('.openModalButton').on('click', function () {
      $('#calendlyModal').fadeIn();
    });

    // Hide modal when close button is clicked
    $('#closeModalButton, .modal-overlay').on('click', function () {
      $('#calendlyModal').fadeOut();
    });

    // Prevent modal from closing when clicking inside the content area
    $('.modal-content').on('click', function (event) {
      event.stopPropagation();
    });
  });
</script>

<script>
  document.addEventListener('DOMContentLoaded', function () {
    const container = document.querySelector('.inner-dropdown-fix');
    const trigger = document.getElementById('sub-toggle-civiles');
    const subList = document.getElementById('sub-list-civiles');

    // English Comment: Desktop Hover Logic
    container.addEventListener('mouseenter', () => {
      if (window.innerWidth > 991) {
        subList.style.display = 'block';
      }
    });

    container.addEventListener('mouseleave', () => {
      if (window.innerWidth > 991) {
        subList.style.display = 'none';
      }
    });

    // English Comment: Mobile Click Logic
    trigger.addEventListener('click', function (e) {
      if (window.innerWidth <= 991) {
        e.preventDefault();
        e.stopPropagation();

        const isOpen = container.classList.contains('w--open');

        if (!isOpen) {
          // English Comment: Set max-height to the exact content height
          subList.style.maxHeight = subList.scrollHeight + "px";
          container.classList.add('w--open');
        } else {
          // English Comment: Close immediately back to 0
          subList.style.maxHeight = "0";
          container.classList.remove('w--open');
        }
      }
    });
  });
</script>